@pytest.mark.asyncio
async def test_get_similar_past_trades_returns_only_outcomes(rag):
    """ConsensusComparator.get_similar_past_trades filters out news."""
    rag.add_news(
        event_id="news_x",
        text="Сбербанк отчитался",
        ts_utc=datetime.now(tz=UTC),
        tickers=["SBER"],
        source="rss",
        source_tier="A",
        headline="news",
        body="",
    )
    indexer = TradeOutcomeIndexer(rag)
    await indexer.index_daily_trades(
        "2026-05-26",
        {
            "trades": [
                _trade(trade_id="win1", ticker="SBER", direction="BUY", pnl_pct=1.5),
                _trade(trade_id="loss1", ticker="SBER", direction="BUY", pnl_pct=-1.0),
            ],
        },
    )
    comp = ConsensusComparator(rag=rag, consensus_today={}, llm_backend="polza")
    results = await comp.get_similar_past_trades(
        ticker="SBER",
        current_setup={
            "direction": "BUY",
            "hmm_regime": "trending",
            "detector": "double_top",
            "tier": "A",
        },
        k=5,
    )
    assert all(r["source"] == TRADE_OUTCOME_SOURCE for r in results)
    assert len(results) >= 1
    assert all("parsed_outcome" in r for r in results)
    outcomes = [r["outcome"] for r in results]
    assert set(outcomes) <= {"win", "loss", "flat"}


def test_historical_edge_multiplier_high_winrate():
    """≥70% WR → 1.3x bonus."""
    similar = [
        {"outcome": "win"},
        {"outcome": "win"},
        {"outcome": "win"},
        {"outcome": "loss"},
    ]
    mult = ConsensusComparator.historical_edge_multiplier(similar)
    assert mult == pytest.approx(1.3)


def test_historical_edge_multiplier_low_winrate():
    """≤30% WR → 0.7x penalty."""
    similar = [
        {"outcome": "loss"},
        {"outcome": "loss"},
        {"outcome": "loss"},
        {"outcome": "win"},
    ]
    mult = ConsensusComparator.historical_edge_multiplier(similar)
    assert mult == pytest.approx(0.7)


def test_historical_edge_multiplier_neutral_mid_winrate():
    """50% WR or insufficient samples → 1.0."""
    mid = [{"outcome": "win"}, {"outcome": "loss"}, {"outcome": "win"}, {"outcome": "loss"}]
    assert ConsensusComparator.historical_edge_multiplier(mid) == pytest.approx(1.0)
    one = [{"outcome": "win"}]
    assert ConsensusComparator.historical_edge_multiplier(one) == pytest.approx(1.0)
    assert ConsensusComparator.historical_edge_multiplier([]) == pytest.approx(1.0)


@pytest.mark.asyncio
async def test_apply_historical_edge_scales_magnitude(rag):
    """End-to-end: index 3 wins → comparator boosts magnitude."""
    indexer = TradeOutcomeIndexer(rag)
    await indexer.index_daily_trades(
        "2026-05-26",
        {
            "trades": [
                _trade(trade_id="w1", ticker="SBER", direction="BUY", pnl_pct=1.2),
                _trade(trade_id="w2", ticker="SBER", direction="BUY", pnl_pct=1.5),
                _trade(trade_id="w3", ticker="SBER", direction="BUY", pnl_pct=0.8),
                _trade(trade_id="l1", ticker="SBER", direction="BUY", pnl_pct=-0.5),
            ],
        },
    )
    comp = ConsensusComparator(rag=rag, consensus_today={}, llm_backend="polza")
    new_mag, debug = await comp.apply_historical_edge(
        ticker="SBER",
        current_setup={
            "direction": "BUY",
            "hmm_regime": "trending",
            "detector": "double_top",
            "tier": "A",
        },
        magnitude=0.5,
        k=10,
    )
    assert debug["wins"] == 3
    assert debug["losses"] == 1
    assert debug["multiplier"] == pytest.approx(1.3)
    assert new_mag == pytest.approx(0.65, abs=1e-3)


