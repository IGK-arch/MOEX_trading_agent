def test_parse_outcome_from_body_roundtrips():
    """Body payload generated by the indexer can be parsed back."""
    body = (
        "type=trade_outcome; date=2026-05-26; outcome=win; ticker=SBER; "
        "direction=BUY; detector=double_top; tier=A; magnitude=0.7200; "
        "pnl_pct=1.2300; holding_min=60"
    )
    parsed = parse_outcome_from_body(body)
    assert parsed["type"] == "trade_outcome"
    assert parsed["outcome"] == "win"
    assert parsed["ticker"] == "SBER"
    assert parsed["detector"] == "double_top"
    assert parsed["magnitude"] == pytest.approx(0.72, abs=1e-3)
    assert parsed["pnl_pct"] == pytest.approx(1.23, abs=1e-3)
    assert parsed["holding_min"] == 60


def test_parse_outcome_from_body_empty():
    """Test parse outcome from body empty."""
    assert parse_outcome_from_body("") == {}
    assert parse_outcome_from_body(None) == {}  # type: ignore[arg-type]


